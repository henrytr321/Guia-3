#include <iostream>

using namespace std;

main()
{
	
	float base, altura, area;
	
	cout<<"Programa para hallar la base de un triangulo"<<endl;
	
	cout<<"Digite la base del triangulo"<<endl;
	
	cin>>base;
	
	cout<<"Digite la altura del triangulo"<<endl;
	
	cin>>altura;
	
	area = (base*altura)/2;
	
	cout<<"El area del triangulo es: "<<area<<endl;
}
